#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	size_t	s_len;
	size_t	new_len;
	char	*tmp;

	i = -1;
	if (s == NULL)
		return (NULL);
	s_len = ft_strlen(s);
	if ((unsigned int)s_len < start)
		return (ft_strdup(""));
	new_len = ft_strlen(s + start);
	if (new_len < len)
		len = new_len;
	if (!(tmp = (char *)malloc(sizeof(char) * (len + 1))))
		return (NULL);
	ft_strlcpy(tmp, s + start, len + 1);
	return (tmp);
}
